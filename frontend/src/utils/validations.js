export const validateNumber = (contact) => {
    var pattern = /^[0-9]*$/;
    return pattern.test(contact);
};

export const validateInputs = (text) => {
    return !!text;
};

export const validateOTP = (otp) => {
    var pattern = /^\d{5}$/;
    return pattern.test(otp);
};

export const validateBusinessName = (name) => {
    if (validateInputs(name) && name.trim().length >= 5) {
        return true;
    }
    return false;
};
